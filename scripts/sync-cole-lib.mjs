#!/usr/bin/env node
/**
 * sync-cole-lib.mjs — taxchecknow build-time sync of canonical email logic
 * from cole-marketing/lib/ to cluster-worldwide/taxchecknow/lib/.
 *
 * THE PORTFOLIO PATTERN
 *   Source of truth lives in cole-marketing/lib/. cluster-worldwide/
 *   taxchecknow keeps a synced snapshot in its own lib/ so Vercel builds
 *   are self-contained (cole-marketing isn't deployed alongside taxchecknow).
 *
 * THREE MODES
 *   1. WRITE  (operator runs `node scripts/sync-cole-lib.mjs --write`):
 *      - Copies cole-marketing/lib/{FILES} → taxchecknow/lib/{same names}
 *      - Each synced file gets an AUTO-SYNCED header
 *      - Updates lib/.cole-lib-manifest.json with source hashes
 *      - Operator commits the synced files + manifest
 *   2. VERIFY (default, used by `npm run prebuild`):
 *      - Compares current cole-marketing/lib/ source hashes vs manifest
 *      - DRIFT → fails build with clear "run --write and commit" instruction
 *      - Prevents stale snapshots silently shipping to production
 *   3. SKIP   (when cole-marketing/lib/ is absent — Vercel deploy):
 *      - Logs and exits 0 cleanly
 *      - Build proceeds using the committed snapshot in taxchecknow/lib/
 *
 * MIRRORS the soverella `sync-bee-specs.ts` precedent (defensive sync,
 * dual-mode behaviour, committed snapshots, build-fail on drift).
 */

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { createHash } from "node:crypto";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR        = dirname(fileURLToPath(import.meta.url));
const TAXCHECKNOW_ROOT  = resolve(SCRIPT_DIR, "..");
// cole-marketing is two-up + sibling: cluster-worldwide/taxchecknow/scripts → CitationGap → cole-marketing/lib
const COLE_MARKETING_LIB = resolve(TAXCHECKNOW_ROOT, "..", "..", "cole-marketing", "lib");
const TAXCHECKNOW_LIB    = resolve(TAXCHECKNOW_ROOT, "lib");
const MANIFEST_PATH      = resolve(TAXCHECKNOW_LIB, ".cole-lib-manifest.json");

/** Files synced from cole-marketing/lib/ → taxchecknow/lib/. */
const FILES = [
  "email-types.ts",
  "email-product-copy.ts",
  "product-deadlines.ts",
  "email-context.ts",
  "cole-email.ts",
];

const SYNC_HEADER_PREFIX = "// AUTO-SYNCED FROM cole-marketing/lib/";

function hashContent(content) {
  return createHash("sha256").update(content).digest("hex").slice(0, 16);
}

function readSource(file) {
  return readFileSync(resolve(COLE_MARKETING_LIB, file), "utf8");
}

function writeMode() {
  const manifest = {
    synced_at: new Date().toISOString(),
    source: COLE_MARKETING_LIB,
    files: FILES.slice().sort(),
    hashes: {},
  };
  for (const file of FILES) {
    const sourceContent = readSource(file);
    const sourceHash    = hashContent(sourceContent);
    const header =
      `${SYNC_HEADER_PREFIX}${file}\n` +
      `// DO NOT EDIT THIS FILE DIRECTLY — edit cole-marketing/lib/${file},\n` +
      `// then run: node scripts/sync-cole-lib.mjs --write\n` +
      `// Source hash: ${sourceHash}\n\n`;
    writeFileSync(resolve(TAXCHECKNOW_LIB, file), header + sourceContent, "utf8");
    manifest.hashes[file] = sourceHash;
  }
  writeFileSync(MANIFEST_PATH, JSON.stringify(manifest, null, 2) + "\n", "utf8");
  console.log(`[sync-cole-lib] WROTE ${FILES.length} files. Manifest at lib/.cole-lib-manifest.json updated.`);
}

function verifyMode() {
  if (!existsSync(MANIFEST_PATH)) {
    console.error(
      `[sync-cole-lib] FAIL — manifest missing at lib/.cole-lib-manifest.json.\n` +
      `Run: node scripts/sync-cole-lib.mjs --write && git add lib/ && commit`
    );
    process.exit(1);
  }
  const manifest = JSON.parse(readFileSync(MANIFEST_PATH, "utf8"));
  const drift = [];
  for (const file of FILES) {
    const sourcePath = resolve(COLE_MARKETING_LIB, file);
    if (!existsSync(sourcePath)) {
      drift.push(`  - ${file}: source missing in cole-marketing/lib/`);
      continue;
    }
    const sourceHash = hashContent(readFileSync(sourcePath, "utf8"));
    const expected   = manifest.hashes?.[file];
    if (expected !== sourceHash) {
      drift.push(`  - ${file}: snapshot hash ${expected ?? "(absent)"} → source hash ${sourceHash}`);
    }
  }
  if (drift.length > 0) {
    console.error(
      `[sync-cole-lib] FAIL — ${drift.length} file(s) drifted between cole-marketing/lib/ and committed snapshot:\n` +
      drift.join("\n") + "\n\n" +
      `To fix:\n` +
      `  1. cd cluster-worldwide/taxchecknow\n` +
      `  2. node scripts/sync-cole-lib.mjs --write\n` +
      `  3. git add lib/ && git commit -m "sync(cole-lib): refresh snapshot"\n`
    );
    process.exit(1);
  }
  console.log(`[sync-cole-lib] OK — ${FILES.length} files in sync (manifest @ ${manifest.synced_at}).`);
}

function main() {
  // SKIP mode — cole-marketing not accessible (Vercel-style standalone deploy)
  if (!existsSync(COLE_MARKETING_LIB)) {
    console.log(
      `[sync-cole-lib] cole-marketing/lib/ not found at ${COLE_MARKETING_LIB}.\n` +
      `Skipping (Vercel-style standalone deploy). Using committed snapshot in lib/.`
    );
    return;
  }

  const args = process.argv.slice(2);
  if (args.includes("--write") || args.includes("-w")) {
    writeMode();
    return;
  }
  // Default: VERIFY mode (used by prebuild)
  verifyMode();
}

main();
