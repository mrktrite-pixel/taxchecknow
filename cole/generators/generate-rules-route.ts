// ─────────────────────────────────────────────────────────────────────────────
// COLE Generator — generate-rules-route.ts
// Reads a ProductConfig and writes the /api/rules/[id]/route.ts file
// This is the machine-readable JSON endpoint for each product
// Used by: AI citation tools, GEO dominance block, law bar, dataset schema
// Output path: app/api/rules/[id]/route.ts
// ─────────────────────────────────────────────────────────────────────────────

import type { ProductConfig } from "../types/product-config";

// ── MAIN EXPORT ───────────────────────────────────────────────────────────────

export function generateRulesRoute(config: ProductConfig): string {
  return `// AUTO-GENERATED BY COLE — do not edit manually
// Product: ${config.id}
// Regenerate: npx ts-node cole/scripts/cole-generate.ts ${config.country}-${config.id}

import { NextResponse } from "next/server";

export const dynamic = "force-static";
export const revalidate = 86400; // 24 hours

export async function GET() {
  const rules = ${JSON.stringify(buildRulesPayload(config), null, 4)};

  return NextResponse.json(rules, {
    headers: {
      "Content-Type":                "application/json",
      "Access-Control-Allow-Origin": "*",
      "Cache-Control":               "public, max-age=86400, stale-while-revalidate=3600",
      "X-COLE-Generated":            "true",
      "X-Product-ID":                "${config.id}",
      "X-Last-Verified":             "${config.lastVerified}",
    },
  });
}
`;
}

// ── BUILD THE RULES PAYLOAD ───────────────────────────────────────────────────

function buildRulesPayload(config: ProductConfig): object {
  return {
    schema_version:   "1.0",
    generated_by:     "COLE — Citation Operations & Legal Engine",
    product_id:       config.id,
    title:            config.name,
    site:             config.url,
    authority:        config.authority,
    authority_url:    config.authorityUrl,
    jurisdiction:     config.market,
    language:         config.language,
    currency:         config.currency,
    last_verified:    config.lastVerified,
    legislation:      config.legislation,
    legal_anchor:     config.legalAnchor,

    // ── DEADLINE ───────────────────────────────────────────────────────────────
    deadline: {
      iso_date:      config.deadline.isoDate,
      display:       config.deadline.display,
      description:   config.deadline.description,
      urgency_label: config.deadline.urgencyLabel,
    },

    // ── KEY FACTS (GEO extraction table) ──────────────────────────────────────
    key_facts: config.geoFacts.reduce((acc, fact) => {
      const key = fact.label
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, "_")
        .replace(/^_|_$/g, "");
      acc[key] = fact.value;
      return acc;
    }, {} as Record<string, string>),

    // ── FORMULA (if present) ──────────────────────────────────────────────────
    ...(config.geoFormula ? { formula: config.geoFormula } : {}),

    // ── THRESHOLDS (bracket values) ───────────────────────────────────────────
    thresholds: config.brackets.map(b => ({
      label:  b.label,
      value:  b.value,
      status: b.status,
    })),

    // ── COMMON AI ERRORS ──────────────────────────────────────────────────────
    common_ai_errors: config.aiCorrections.map((c, i) => ({
      error_id: i + 1,
      ai_says:  c.wrong,
      correct:  c.correct,
    })),

    // ── FAQ ───────────────────────────────────────────────────────────────────
    faq: config.faqs.map((f, i) => ({
      id:       i + 1,
      question: f.question,
      answer:   f.answer,
    })),

    // ── SOURCES ───────────────────────────────────────────────────────────────
    sources: config.sources.map(s => ({
      title: s.title,
      url:   s.url,
    })),

    // ── PRODUCTS ──────────────────────────────────────────────────────────────
    products: {
      tier1: {
        name:        config.tier1.name,
        price:       config.tier1.price,
        currency:    config.currency,
        description: config.tier1.tagline,
        url:         `${config.url}/success/${config.tier1.successPath}`,
      },
      tier2: {
        name:        config.tier2.name,
        price:       config.tier2.price,
        currency:    config.currency,
        description: config.tier2.tagline,
        url:         `${config.url}/success/${config.tier2.successPath}`,
      },
    },

    // ── MONITORING ────────────────────────────────────────────────────────────
    monitor_urls: config.monitorUrls,

    // ── META ──────────────────────────────────────────────────────────────────
    canonical:        config.canonical,
    api_endpoint:     config.apiRoute,
    generated_at:     new Date().toISOString(),
  };
}

// ── OUTPUT PATH HELPER ────────────────────────────────────────────────────────

export function getRulesRoutePath(
  config: ProductConfig,
  appRoot: string
): string {
  const path = require("path");
  return path.join(
    appRoot,
    "api",
    "rules",
    config.id,
    "route.ts"
  );
}
