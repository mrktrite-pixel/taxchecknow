function daysToDeadline(): number {
  if (!DEADLINE_ISO) return 0;
  const now = new Date();
  const end = new Date(DEADLINE_ISO);
  return Math.max(0, Math.ceil((end.getTime() - now.getTime()) / 86_400_000));
}

